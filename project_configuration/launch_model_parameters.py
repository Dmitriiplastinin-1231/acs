"""launch_model_parameters.py

Единое место, где собраны ВСЕ численные параметры задачи.

Идея этого файла простая:
1) здесь мы храним физические параметры постановки;
2) здесь же храним численные параметры интегрирования;
3) отсюда остальные файлы берут исходные константы и вспомогательные формулы.

Такой подход удобен на защите: можно открыть один файл и сразу показать,
откуда в программе берутся A, T, τ, шаг h, высоты 10 км и 160 км.
"""
from __future__ import annotations

import argparse
import math


# =============================================================================
# 1. ПАРАМЕТРЫ ПОСТАНОВКИ ЗАДАЧИ
# =============================================================================
# A_ROLL — амплитуда качки платформы.
# По условию максимальный наклон платформы равен 2 градуса.
# В вычислениях удобнее работать в радианах, поэтому сразу переводим градусы в рад.
A_ROLL = math.radians(2.0)

# T_ROLL — период качки платформы, с.
# По условию: 8 секунд.
T_ROLL = 8.0

# TAU — задержка системы управления TVC, с.
# Это означает, что корректирующее действие сейчас строится по данным,
# которые были измерены 0.3 секунды назад.
TAU = 0.3


# =============================================================================
# 2. УПРОЩЁННАЯ ВЕРТИКАЛЬНАЯ КИНЕМАТИКА
# =============================================================================
# A_Z — принятое в модели среднее вертикальное ускорение ракеты, м/с².
# Это не претендует на полный реализм реальной ракеты, а используется как простая,
# понятная и воспроизводимая кинематическая модель для учебной задачи.
# При таком допущении:
#   H(t) = 0.5 * A_Z * t^2
#   v(t) = A_Z * t
A_Z = 10.0


# =============================================================================
# 3. ПАРАМЕТРЫ УПРАВЛЕНИЯ
# =============================================================================
# В презентации используется уравнение:
#   dω/dt = -a * θ(t - τ)
# где a = k / I.
# Здесь мы задаём именно эту приведённую константу a.
A_CTRL = 0.05


# =============================================================================
# 4. ЧИСЛЕННЫЕ ПАРАМЕТРЫ МЕТОДА
# =============================================================================
# DT — шаг интегрирования по времени, с.
# Он выбран так, чтобы задержка 0.3 с точно укладывалась в целое число шагов:
#   0.3 / 0.01 = 30 шагов.
DT = 0.01

# Высота для вопроса 1.
H_10KM = 10_000.0

# Высота для вопроса 2 (орбитальный участок в постановке задачи).
H_160KM = 160_000.0

# Базовое число фаз для sweep.
# 8 точек = через каждые 45 градусов.
N_PHASES_SMALL = 8

# Более плотная сетка для проверки, что результат не меняется слишком сильно
# при сгущении фаз.
N_PHASES_LARGE = 72


# =============================================================================
# 5. ВСПОМОГАТЕЛЬНЫЕ ФОРМУЛЫ
# =============================================================================

def omega_roll(period: float = T_ROLL) -> float:
    """Возвращает круговую частоту качки Ω = 2π / T.

    Важно не путать:
    - Ω (большая омега) — это характеристика самого процесса качки;
    - ω (маленькая омега) — это угловая скорость наклона платформы в данный момент.
    """
    return 2.0 * math.pi / period



def height_at(t: float, a_z: float = A_Z) -> float:
    """Высота ракеты в момент времени t.

    Используем простую кинематическую модель с постоянным вертикальным ускорением:
        H(t) = 0.5 * a_z * t^2
    """
    return 0.5 * a_z * t * t



def vertical_speed_at(t: float, a_z: float = A_Z) -> float:
    """Вертикальная скорость ракеты в момент времени t.

    При постоянном вертикальном ускорении:
        v(t) = a_z * t
    """
    return a_z * t



def time_to_height(height_m: float, a_z: float = A_Z) -> float:
    """Время, за которое ракета достигает заданной высоты.

    Из формулы:
        H = 0.5 * a_z * t^2
    получаем:
        t = sqrt(2H / a_z)
    """
    return math.sqrt(2.0 * height_m / a_z)



def initial_conditions(
    phi0: float,
    amplitude: float = A_ROLL,
    period: float = T_ROLL,
) -> tuple[float, float]:
    """Начальные условия от фазы качки платформы.

    Если качка описывается как:
        θ_пл(t) = A * cos(Ωt + φ0),
    то в момент старта t = 0 получаем:
        θ0 = A * cos(φ0)
        ω0 = dθ_пл/dt|_{t=0} = -A * Ω * sin(φ0)

    Возвращаем кортеж:
        (theta0, omega0)
    где
        theta0 — начальный угол наклона платформы,
        omega0 — начальная угловая скорость изменения наклона.
    """
    omega_big = omega_roll(period)
    theta0 = amplitude * math.cos(phi0)
    omega0 = -amplitude * omega_big * math.sin(phi0)
    return theta0, omega0


# =============================================================================
# 6. ПАРСЕР АРГУМЕНТОВ КОМАНДНОЙ СТРОКИ
# =============================================================================
# Этот helper нужен, чтобы все скрипты принимали одни и те же аргументы:
# A, T, τ, a, dt, az.
# Тогда любой файл можно запустить отдельно и переопределить параметры через CLI.

def build_parser(description: str) -> argparse.ArgumentParser:
    """Создаёт стандартный argparse-парсер для всех сценариев проекта."""
    parser = argparse.ArgumentParser(description=description)
    parser.add_argument(
        "--A",
        type=float,
        default=A_ROLL,
        help=f"Амплитуда качки, рад (по умолчанию {A_ROLL:.6f})",
    )
    parser.add_argument(
        "--T",
        type=float,
        default=T_ROLL,
        help=f"Период качки, с (по умолчанию {T_ROLL})",
    )
    parser.add_argument(
        "--tau",
        type=float,
        default=TAU,
        help=f"Задержка TVC, с (по умолчанию {TAU})",
    )
    parser.add_argument(
        "--a",
        type=float,
        default=A_CTRL,
        help=f"Коэффициент a = k / I, 1/с² (по умолчанию {A_CTRL})",
    )
    parser.add_argument(
        "--dt",
        type=float,
        default=DT,
        help=f"Шаг интегрирования, с (по умолчанию {DT})",
    )
    parser.add_argument(
        "--az",
        type=float,
        default=A_Z,
        help=f"Вертикальное ускорение, м/с² (по умолчанию {A_Z})",
    )
    return parser
